package com.github.kozyr.android.meatballs.map.directions;

public class DirectionFetchException extends Exception {

	public DirectionFetchException() {
		super();
	}

	public DirectionFetchException(String message, Throwable cause) {
		super(message, cause);
	}

	public DirectionFetchException(String message) {
		super(message);
	}

	public DirectionFetchException(Throwable cause) {
		super(cause);
	}

}
